package designpatterns_v2.structural.composite;

public abstract class BaseShape implements Graphic{
    protected int x,y;

    public BaseShape() {
    }

    public BaseShape(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public void move(int x, int y) {
        this.x += x;
        this.y += y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
